package com.nltt.pojo;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author tadai
 */
@Entity
@Table(name = "post_photo")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "PostPhoto.findAll", query = "SELECT p FROM PostPhoto p"),
    @NamedQuery(name = "PostPhoto.findById", query = "SELECT p FROM PostPhoto p WHERE p.id = :id"),
    @NamedQuery(name = "PostPhoto.findByPhoto", query = "SELECT p FROM PostPhoto p WHERE p.photo = :photo")})
public class PostPhoto implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Size(max = 200)
    @Column(name = "photo")
    private String photo;
    @JoinColumn(name = "postId", referencedColumnName = "PostID")
    @ManyToOne(optional = false)
    private Posts postId;

    public PostPhoto() {
    }

    public PostPhoto(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public Posts getPostId() {
        return postId;
    }

    public void setPostId(Posts postId) {
        this.postId = postId;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof PostPhoto)) {
            return false;
        }
        PostPhoto other = (PostPhoto) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.nltt.pojo.PostPhoto[ id=" + id + " ]";
    }
    
}
